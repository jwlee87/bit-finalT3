package kr.co.coily.repository.vo;

public class FileGroupVO {
	
	private int fileGroupNo;
	private String fileGroupType;
	private int fileGroupKey;
	
	public int getFileGroupNo() {
		return fileGroupNo;
	}
	public void setFileGroupNo(int fileGroupNo) {
		this.fileGroupNo = fileGroupNo;
	}
	public String getFileGroupType() {
		return fileGroupType;
	}
	public void setFileGroupType(String fileGroupType) {
		this.fileGroupType = fileGroupType;
	}
	public int getFileGroupKey() {
		return fileGroupKey;
	}
	public void setFileGroupKey(int fileGroupKey) {
		this.fileGroupKey = fileGroupKey;
	}
	
}
